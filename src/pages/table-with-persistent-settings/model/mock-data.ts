// Generate 100 records by repeating base campaigns with variations
export const mockTableData: TableWithPersistentSettingsItem[] = Array.from({ length: 100 }, (_, index) => {
	const base = baseCampaigns[index % baseCampaigns.length]
	const multiplier = Math.floor(index / baseCampaigns.length) + 1
	
	// Variations for different records
	const variations = [
		{ name: `${base.name} v${multiplier}`, budget: base.budget * multiplier },
		{ name: `${base.name} ${multiplier}`, budget: base.budget * (multiplier * 0.8) },
		{ name: `${base.name}-${multiplier}`, budget: base.budget * (multiplier * 1.2) },
	]
	
	const variation = variations[index % variations.length]
	const date = new Date(base.created_at)
	date.setDate(date.getDate() + index)
	
	return {
		...base,
		id: index + 1,
		name: variation.name,
		budget: Math.round(variation.budget),
		cost: Math.round(base.cost * multiplier * 0.7),
		views: Math.round(base.views * multiplier * 0.8),
		clicks: Math.round(base.clicks * multiplier * 0.8),
		ctr: base.ctr + (index % 3) * 0.1,
		cpc: base.cpc + (index % 5) * 0.5,
		orders: Math.round(base.orders * multiplier * 0.8),
		created_at: date.toISOString().split('T')[0],
		status: base.status,
	}
})
